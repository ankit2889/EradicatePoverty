    public boolean validateProfileDetails()
    {
        boolean errorFlag=false;
        
        if(!Validation.validateTextFieldsForNonEmpty(firstNameJtext)||firstNameJtext.getText().trim().equalsIgnoreCase("First name"))
        {
        errorFlag=true;
        firstNameValidatorJlbl.setText("Please enter first name");
            
        }
        
        else if(!Validation.validateTextFieldsForString(firstNameJtext))
        {
            errorFlag=true;
            firstNameValidatorJlbl.setText("Enter valid name");
        }
        
        
        if(!Validation.validateTextFieldsForNonEmpty(lastNameJtext)||lastNameJtext.getText().trim().equalsIgnoreCase("Last name"))
        {
        errorFlag=true;
        lastNameValidatorJlbl.setText("Please enter last name");
            
        }
        
        else if(!Validation.validateTextFieldsForString(lastNameJtext))
        {
            errorFlag=true;
            lastNameValidatorJlbl.setText("Enter valid name");
        }
        
        if(!Validation.validateTextFieldsForString(middleNameJtext))
        {
            errorFlag=true;
            middleNameValidatorJlbl.setText("Enter valid name");
        }
        
        
        if(String.valueOf(birthMonthComboBox.getSelectedItem()).equalsIgnoreCase("MM"))
        {
            errorFlag=true;
            monthValidatorJlbl.setText("Please select month");
        }
        
        if(String.valueOf(birthDateComboBox.getSelectedItem()).equalsIgnoreCase("DD"))
        {
            errorFlag=true;
            dateValidatorJlbl.setText("Please select date");
        }
        
        
        if(String.valueOf(birthYearComboBox.getSelectedItem()).equalsIgnoreCase("YYYY"))
        {
            errorFlag=true;
            yearValidatorJlbl.setText("Please select year");
        }
        
        
        if(!Validation.validateTextFieldsForString(idNoJtext)||idNoJtext.getText().trim().equalsIgnoreCase("Id no:"))
        {
            errorFlag=true;
            idNoValidatorJlbl.setText("Enter valid id no:");
        }
        
        if(!Validation.validateTextFieldsForPhoneNumber(primaryPhoneNoJtext))
        {
            errorFlag=true;
            primaryContactNoValidatorJlbl.setText("Enter valid primary contact no.");
        }
        
        
        if(!Validation.validateTextFieldsForPhoneNumber(secondaryPhoneNoJtext))
        {
            errorFlag=true;
            secondaryContactNoValidatorJlbl.setText("Enter valid secondary contact no.");
        }
        
        
        if(!Validation.validateTextFieldsForString(streetJtext)||streetJtext.getText().trim().equalsIgnoreCase("Street"))
        {
            errorFlag=true;
            addressValidatorJlbl.setText("Please enter valid street");
        }
        
        
        if(!Validation.validateTextFieldsForString(cityJtext)||cityJtext.getText().trim().equalsIgnoreCase("City"))
        {
            errorFlag=true;
            cityValidatorJlbl.setText("Please enter valid city");
        }
        
        if(!Validation.validateTextFieldsForString(stateJtext)||stateJtext.getText().trim().equalsIgnoreCase("State"))
        {
            errorFlag=true;
            stateValidatorJlbl.setText("Please enter valid state");
        }
        
        if(!Validation.validateTextFieldsForInteger(zipcodeJtext)||zipcodeJtext.getText().trim().equalsIgnoreCase("Zip code"))
        {
            errorFlag=true;
            zipCodeValidatorJlbl.setText("Please enter valid zipcode");
        }
        
        
        return errorFlag;
        
        
    }
    public boolean validateAccountForm()
    {
        boolean errorFlag=false;
        
        if(!Validation.validateTextFieldForPassword(passwordJtext)||passwordJtext.getPassword().equals(""))
        {
            errorFlag=true;
            passwordValidatorJlbl.setText("Please enter valid password");
            passwordValidatorJlbl.setVisible(true);
        }
        
        return errorFlag;
    }

