    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        hideAllLabels();
       if(!validateProfileDetails())
       {
           userAccount.getPerson().setFirstName(firstNameJtext.getText().trim());
           userAccount.getPerson().setLastName(lastNameJtext.getText().trim());
           if(!middleNameJtext.getText().trim().equalsIgnoreCase("Middle name:"))
           {
               userAccount.getPerson().setMiddleName(middleNameJtext.getText().trim());
           }
           
         Calendar c = new GregorianCalendar();
         c.set(Integer.parseInt(String.valueOf(birthYearComboBox.getSelectedItem())),
                 Integer.parseInt(String.valueOf(birthMonthComboBox.getSelectedItem())),
                 Integer.parseInt(String.valueOf(birthDateComboBox.getSelectedItem())));
         
           userAccount.getPerson().setDateOfBirth(new Date(c.getTimeInMillis()));
           userAccount.getPerson().setIdType(String.valueOf(idComboBox.getSelectedItem()));
           userAccount.getPerson().setUniqueId(idNoJtext.getText().trim());
           userAccount.getPerson().setPrimaryContactNo(primaryPhoneNoJtext.getText().trim());
           userAccount.getPerson().setSecondaryContactNo(secondaryPhoneNoJtext.getText().trim());
           userAccount.getPerson().getAddress().setStreetName(streetJtext.getText().trim());
           userAccount.getPerson().getAddress().setCityName(cityJtext.getText().trim());
           userAccount.getPerson().getAddress().setStateName(stateJtext.getText().trim());
           
           JOptionPane.showMessageDialog(null,"All the details are successfully updated","Data updated",JOptionPane.INFORMATION_MESSAGE);
           return;
           
       }
    }//GEN-LAST:event_jButton3ActionPerformed

