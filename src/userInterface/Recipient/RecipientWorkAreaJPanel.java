    public boolean validateForm()
    {
        boolean errorFlag=false;
        
        if(!Validation.validateTextFieldForCCV(mpinJtext))
        {
            mpinValidatorJlbl.setText("Please enter valid pin");
            errorFlag=true;
        }
        
        
        
        return errorFlag;
    }
    
    
    public boolean authenticateUser(String password)
    {
        boolean validUser=true;
        int counter=0;
        if(counter==5)
        {
        validUser=false;    
        userAccount.setIsActive(false);
        }
        
        else if(!recipient.getMpin().equalsIgnoreCase(password))
        {
            validUser=false;
            mpinValidatorJlbl.setText("Incorrect pin.");
            mpinValidatorJlbl2.setText("Account will be locked after 5 attempts");
            counter++;
        }
        
        return validUser;
    }

