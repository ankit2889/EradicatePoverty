    private void goJbutton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_goJbutton1ActionPerformed
        // TODO add your handling code here:
        
        int selectedIndex = menuJlist.getSelectedIndex();
        int a=1;
        if(selectedIndex>=0)
        {
         if(((String)menuJlist.getSelectedValue()).equalsIgnoreCase("Inbox"))
                {
                    CheckInboxJPanel checkInboxJPanel = new CheckInboxJPanel(userProcessContainer,userAccount);
                    
                    mpinJtext.setText("");
                    CardLayout layout = (CardLayout)userProcessContainer.getLayout();
                    userProcessContainer.add("checkInboxJPanel",checkInboxJPanel);
                    layout.next(userProcessContainer);
                }
         
         
          
         
         else if(((String)menuJlist.getSelectedValue()).equalsIgnoreCase("Check Account Balance"))
                {
                    CheckAccountBalanceJPanel checkAccountBalanceJPanel = new CheckAccountBalanceJPanel(userProcessContainer,recipient);
                    
                    CardLayout layout = (CardLayout)userProcessContainer.getLayout();
                    userProcessContainer.add("checkAccountBalanceJPanel",checkAccountBalanceJPanel);
                    mpinJtext.setText("");
                    layout.next(userProcessContainer);
                }
         
         else if(((String)menuJlist.getSelectedValue()).equalsIgnoreCase("Forgot MPIN"))
                {
                    
                    ForgotPasswordJPanel forgotPasswordJPanel = new ForgotPasswordJPanel(userProcessContainer,userAccount);
                   
                    CardLayout layout = (CardLayout)userProcessContainer.getLayout();
                    userProcessContainer.add("forgotPasswordJPanel",forgotPasswordJPanel);
                    mpinJtext.setText("");
                    layout.next(userProcessContainer);
                }
         
         
         else if(((String)menuJlist.getSelectedValue()).equalsIgnoreCase("WithDraw Money"))
                { 
                    enterPinJPanel.setVisible(true);
        }
         
         
         
        }else
        {
            JOptionPane.showMessageDialog(displayJPanel,"Please select option from list to contine","No Selection",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_goJbutton1ActionPerformed

