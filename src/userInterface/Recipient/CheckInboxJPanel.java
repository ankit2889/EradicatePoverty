    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        int selectedRow = newRequestsJtable.getSelectedRow();
        
        if(selectedRow>=0)
        {
            WorkRequest workRequest = (WorkRequest)newRequestsJtable.getValueAt(selectedRow,0);
            Payment payment = null;
            
            if (workRequest instanceof Payment)
            {
                payment = (Payment)workRequest;
                amountJtxt.setText(String.valueOf(payment.getAmount()));
                agentOutletNameJtxt.setText((payment.getLocalAgent().getOutletName()));
                
            }
        }
        
        else
        {
            JOptionPane.showMessageDialog(displayJPanel,"Please select donation item to view details","No Selection",JOptionPane.ERROR_MESSAGE);
            return;
        }
    }//GEN-LAST:event_jButton1ActionPerformed


