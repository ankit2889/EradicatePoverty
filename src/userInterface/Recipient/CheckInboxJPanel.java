    public void populateNewRequestsTable()
    {
        DefaultTableModel dtm = (DefaultTableModel)newRequestsJtable.getModel();
        dtm.setRowCount(0);
        
        for(WorkRequest workRequest:userAccount.getWorkQueue().getWorkRequestList())
        {
            if(workRequest instanceof Payment)
            {
                Payment payment = (Payment)workRequest;
               Object row[]= new Object[2];
               row[0]=payment;
               row[1]=payment.getAmount();
              
               dtm.addRow(row);
            }
        }
    }

